#include <stdio.h>
/**
 * main - Entry point
 *
 * Description: main prints alphabet in lowercase then uppercase
 * followed by new line
 * Return: Always 0 (success)
 */
int main(void)
{
	char letter;

	for (letter = 'a'; letter <= 'z'; letter++)
		putchar(letter);
	for (letter = 'A'; letter <= 'Z'; letter++)
		putchar(letter);
	putchar('\n');
	return (0);
}
